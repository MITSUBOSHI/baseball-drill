// 長い語を先にマッチさせるためソート
const sorted = entries.sort((a, b) => b[0].length - a[0].length);

// 正規表現を事前コンパイル
const pattern = new RegExp(
  `(${sorted.map(([word]) => word.replace(/[.*+?^${}()|[\]\\]/g, "\\$&")).join("|")})`,
  "g"
);

export type FuriganaSegment =
  | { type: "text"; value: string }
  | { type: "ruby"; base: string; reading: string };

const dict = new Map(entries);

function isKana(ch: string): boolean {
  const code = ch.charCodeAt(0);
  // ひらがな (3040-309F), カタカナ (30A0-30FF), 長音記号も含む
  return (code >= 0x3040 && code <= 0x309f) || (code >= 0x30a0 && code <= 0x30ff);
}

// 漢字部分のみにrubyを付け、かな部分はtextとして返す
function splitRuby(base: string, reading: string): FuriganaSegment[] {
  const segments: FuriganaSegment[] = [];
  let bi = 0;
  let ri = 0;

  while (bi < base.length) {
    if (isKana(base[bi])) {
      let kana = "";
      while (bi < base.length && isKana(base[bi])) {
        kana += base[bi];
        bi++;
        ri++;
      }
      segments.push({ type: "text", value: kana });
    } else {
      let kanji = "";
      while (bi < base.length && !isKana(base[bi])) {
        kanji += base[bi];
        bi++;
      }
      if (bi < base.length) {
        // 次のかな文字がreadingのどこに現れるか探して、そこまでを漢字の読みとする
        const nextKana = base[bi];
        const nextKanaPos = reading.indexOf(nextKana, ri);
        if (nextKanaPos >= 0) {
          segments.push({ type: "ruby", base: kanji, reading: reading.slice(ri, nextKanaPos) });
          ri = nextKanaPos;
        } else {
          segments.push({ type: "ruby", base: kanji, reading: reading.slice(ri) });
          ri = reading.length;
        }
      } else {
        segments.push({ type: "ruby", base: kanji, reading: reading.slice(ri) });
        ri = reading.length;
      }
    }
  }

  return segments;
}

export function annotateFurigana(text: string): FuriganaSegment[] {
  const segments: FuriganaSegment[] = [];
  let lastIndex = 0;

  for (const match of text.matchAll(pattern)) {
    const matchIndex = match.index!;
    if (matchIndex > lastIndex) {
      segments.push({ type: "text", value: text.slice(lastIndex, matchIndex) });
    }
    const word = match[0];
    const reading = dict.get(word)!;
    segments.push(...splitRuby(word, reading));
    lastIndex = matchIndex + word.length;
  }

  if (lastIndex < text.length) {
    segments.push({ type: "text", value: text.slice(lastIndex) });
  }

  return segments;
}
